int	stack_size(char **arguments)
{
	int	i;

	i = 0;
	while (arguments[i] != NULL)
		i++;
	return (i);
}

int	is_matched(char *s1, char *s2)
{
	int	i;

	i = 0;
	while (s1[i] || s2[i])
	{
		if (s1[i] != s2[i])
			return (FALSE);
		i++;
	}
	return (TRUE);
}
